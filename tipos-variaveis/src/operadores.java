public class operadores {

    public static void main(String[] args) {
        boolean opcao1 = true;
        boolean opcao2 = false;

        if (opcao1 && opcao2) {
            System.out.println("As duas opções são verdadeiras!");
        } else {
            System.out.println("Nenhuma ou alguma das opções não é verdadeira");
        }
    }
}